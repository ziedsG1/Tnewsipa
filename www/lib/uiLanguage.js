  function getUiLangId() {
    const id = localStorage.getItem(STORAGE_KEY) || "ar";
    return UI_LANGS[id] ? id : "ar";
  }

  function getUiLang() {
    return UI_LANGS[getUiLangId()];
  }

  function setUiLang(id) {
    if (UI_LANGS[id]) localStorage.setItem(STORAGE_KEY, id);
  }

  function t(key, vars) {
    const pack = STRINGS[getUiLangId()] || STRINGS.ar;
    let s = pack[key] ?? STRINGS.ar[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
      });
    }
    return s;
  }

  function topicLabel(topicKey) {
    const map = {
      sport: "topicSport",
      economy: "topicEconomy",
      politics: "topicPolitics",
      culture: "topicCulture",
      world: "topicWorld",
      tunisia: "topicTunisia",
      general: "topicGeneral",
    };
    return t(map[topicKey] || "topicGeneral");
  }

  function applyDocumentLocale() {
    const lang = getUiLang();
    document.documentElement.lang = lang.id === "tn" ? "ar" : lang.id;
    document.documentElement.dir = lang.dir;
  }

  function formatLocaleDate(date, options) {
    return date.toLocaleString(getUiLang().locale, options);
  }

  window.TnewsUi = {
    UI_LANGS,
    getUiLangId,
    getUiLang,
    setUiLang,
    t,
    topicLabel,
    applyDocumentLocale,
    formatLocaleDate,
  };
